#Crea un programa que pida tres números por teclado. El programa imprime en consola la media aritmética de los números introducidos

print("Te vamos a pedir 3 números para hacer su media")

n1=int(input("Introduce tu primer número: "))
n2=int(input("Introduce tu segundo número: "))
n3=int(input("Introduce tu tercer número: "))

media=(n1+n2+n3)/3

print("La media de esos tres números es: ")
print(media)
